package ex_18042024;

public class Lab009_Compound {
    public static void main(String[] args) {

        // Compound Operator  Those all are self-explanatory

        int b1 = 90;
        b1 -= 89;   // b1 = b1 - 89
        System.out.println(b1);
        int b2 = 90;
        b2 += 77; // b2 = b2+77
        System.out.println(b2);

        int b3 = 70;
        b3 *= 2; // b3 = b3*70
        System.out.println(b3);

        int b4 = 70;
        b4 /= 5; // b4 = b4/=70
        System.out.println(b4);

        int b5 = 80;
        b5 %= 3; // b5 = b5%= 3;
        System.out.println(b5);

    }
}
